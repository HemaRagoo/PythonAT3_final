class Item:
    def __init__(self, name, description):
        """
        Initialize an Item object.

        Args:
            name (str): The name of the item.
            description (str): The description of the item.

        Attributes:
            name (str): The name of the item.
            description (str): The description of the item.
        """
        self.name = name
        self.description = description
